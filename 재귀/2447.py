Num=int(input())

def star(n):
    if n==3: 
        return['***','* *','***'] 
    a=star(n//3) 
    stars=[]

    for i in a:
        stars.append(i*3)
    for i in a:
        stars.append(i+' '*(n//3)+i)
    for i in a:
        stars.append(i*3)
    return stars

print('\n'.join(star(Num)))


